#ifndef CHAOS_MEMORY_H
#define CHAOS_MEMORY_H

#include "core/chaos_types.h"

/* ************* STRUCTURES ************* */

/**
 * @brief Simple linear memory arena for fast allocations.
 *
 * Note: This allocator does not support deallocation of individual blocks.
 * All memory is freed when the arena is reset or destroyed.
 */
typedef struct ce_arena_s
{
    ce_u8*  base; /*< Pointer to the start of the memory block */
    ce_size cap;  /*< Total capacity of the memory block in bytes */
    ce_size off;  /*< Current offset in bytes from the base */
} ce_arena;

/* ************* FUNCTION DECLARATIONS ************* */

/**
 * @brief Initializes a memory arena with a given memory block and capacity.
 * @param a Pointer to the ce_arena structure to initialize.
 * @param memory Pointer to the pre-allocated memory block.
 * @param capacity Size of the memory block in bytes.
 * @return None.
 */
void ce_arena_init(ce_arena* a, void* memory, ce_size capacity);

/**
 * @brief Allocates a block of memory from the arena with specified size and alignment.
 * @param a Pointer to the ce_arena structure.
 * @param size Size of the memory block to allocate in bytes.
 * @param align Alignment requirement (must be a power of two).
 * @return Pointer to the allocated memory block, or NULL if allocation fails.
 */
void* ce_arena_alloc(ce_arena* a, ce_size size, ce_size align);

/**
 * @brief Resets the arena, making all previously allocated memory available again.
 * @param a Pointer to the ce_arena structure to reset.
 * @return None.
 */
void ce_arena_reset(ce_arena* a);

#endif /* CHAOS_MEMORY_H */
